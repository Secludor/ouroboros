_TOML_RELATIVE_PATH = Path(".ouroboros") / "mechanical.toml"
_DETECT_MARKER = "# auto-generated by ouroboros evaluate detector"

_CommandKey = Literal["lint", "build", "test", "static", "coverage"]
_COMMAND_KEYS: tuple[_CommandKey, ...] = ("lint", "build", "test", "static", "coverage")

# Cap on raw manifest bytes sent to the LLM so a giant monorepo README cannot
# blow the prompt budget. The detector only needs structural hints.
_MANIFEST_EXCERPT_BYTES = 4000

# Top-level files that meaningfully hint at a project's toolchain. We stream
# only these into the prompt — not the whole tree — to keep the call cheap
# and deterministic.
_MANIFEST_CANDIDATES: tuple[str, ...] = (
    "package.json",
    "pnpm-workspace.yaml",
    "pyproject.toml",
    "uv.lock",
    "requirements.txt",
    "setup.py",
    "setup.cfg",
    "Cargo.toml",
    "go.mod",
    "pom.xml",
    "build.gradle",
    "build.gradle.kts",
    "Makefile",
    "justfile",
    "Taskfile.yml",
    "build.zig",
    "composer.json",
    "Gemfile",
    "mix.exs",
    "deno.json",
    "biome.json",
    "eslint.config.js",
    ".eslintrc.json",
    "tsconfig.json",
)


@dataclass(frozen=True, slots=True)
class DetectedCommands:
    """Validated command proposals ready for persistence."""

    lint: str | None = None
    build: str | None = None
    test: str | None = None
    static: str | None = None
    coverage: str | None = None

    def is_empty(self) -> bool:
        return all(
            getattr(self, key) is None for key in ("lint", "build", "test", "static", "coverage")
        )


def toml_path(working_dir: Path) -> Path:
    """Return the canonical path to ``.ouroboros/mechanical.toml``."""
    return working_dir / _TOML_RELATIVE_PATH


def has_mechanical_toml(working_dir: Path) -> bool:
    """Return True iff ``.ouroboros/mechanical.toml`` already exists."""
    return toml_path(working_dir).is_file()


async def ensure_mechanical_toml(
    working_dir: Path,
    adapter: LLMAdapter,
    *,
    model: str | None = None,
    backend: str | None = None,
    force: bool = False,
) -> bool:
    """Write ``.ouroboros/mechanical.toml`` via one AI detect call.

    Idempotent: when the toml already exists and ``force`` is False, returns
    True immediately. Otherwise makes one LLM call, validates the proposals,
    and persists the file. Never raises — returns False on any failure.

    Args:
        working_dir: Project root to detect against.
        adapter: LLM adapter used for the single detect call.
        model: Explicit model identifier. When ``None``, resolved via
            ``get_mechanical_detector_model(backend)`` so Codex / OpenCode
            backends receive the ``"default"`` sentinel instead of a Claude
            model name they cannot route.
        backend: Optional backend hint used when ``model`` is ``None``.
        force: If True, re-detect and overwrite an existing toml.

    Returns:
        True on success (toml exists after the call), False otherwise.
    """
    if model is None:
        from ouroboros.config.loader import get_mechanical_detector_model

        model = get_mechanical_detector_model(backend=backend)
    target = toml_path(working_dir)
    if target.is_file() and not force:
        return True

    manifests = _collect_manifests(working_dir)
    if not manifests:
        log.info("detector.skipped", reason="no_manifests", working_dir=str(working_dir))
        return False

    proposal = await _ask_llm(adapter, working_dir, manifests, model)
    if proposal is None:
        return False

    validated = _validate_proposal(working_dir, proposal)
    if validated.is_empty():
        log.info(
            "detector.empty_after_validation",
            working_dir=str(working_dir),
            proposal=_redact(proposal),
        )
        return False

    try:
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(_render_toml(validated), encoding="utf-8")
    except OSError as exc:
        log.warning("detector.write_failed", path=str(target), error=str(exc))
        return False

    log.info(
        "detector.toml_written",
        path=str(target),
        commands={k: getattr(validated, k) for k in _COMMAND_KEYS},
    )
    return True


def _collect_manifests(working_dir: Path) -> dict[str, str]:
    """Read top-level manifest files into a name → text-excerpt mapping."""
    collected: dict[str, str] = {}
    for name in _MANIFEST_CANDIDATES:
        candidate = working_dir / name
        if not candidate.is_file():
            continue
        try:
            raw = candidate.read_bytes()[:_MANIFEST_EXCERPT_BYTES]
            collected[name] = raw.decode("utf-8", errors="replace")
        except OSError:
            continue
    return collected


_SYSTEM_PROMPT = """You are inspecting a software repository to propose the commands Ouroboros should run as zero-cost Stage 1 verification (lint, build, test, static analysis, coverage).

Rules:
1. Propose only commands that this project can actually execute right now, based on the manifest excerpts provided. Do not invent scripts that are not declared.
2. Prefer the project's own conventions: npm/pnpm/yarn scripts, uv/pytest, cargo, go, make, just, etc.
3. If a category (e.g. coverage) has no clear command, OMIT it from the JSON — do not guess.
4. Every command must start with a well-known tool. Absolute paths and shell operators (`&&`, `||`, pipes, redirects) are forbidden.
5. Output ONLY a JSON object with at most the keys: lint, build, test, static, coverage. Values are single-line shell command strings."""


async def _ask_llm(
    adapter: LLMAdapter,
    working_dir: Path,
    manifests: dict[str, str],
    model: str,
) -> dict[str, str] | None:
    """Send one detect prompt and return a parsed JSON proposal."""
    payload_parts = [f"Working directory: {working_dir.name}", ""]
    for name, body in manifests.items():
        payload_parts.append(f"--- {name} ---")
        payload_parts.append(body.rstrip())
        payload_parts.append("")
    payload_parts.append(
        "Return a JSON object mapping at most {lint, build, test, static, coverage} to"
        " shell command strings. Omit keys you cannot verify."
    )

    messages = [
        Message(role=MessageRole.SYSTEM, content=_SYSTEM_PROMPT),
        Message(role=MessageRole.USER, content="\n".join(payload_parts)),
    ]
    config = CompletionConfig(
        model=model,
        temperature=0.0,
        max_tokens=512,
        response_format={"type": "json_object"},
    )
    result = await adapter.complete(messages, config)
    if result.is_err:
        log.warning("detector.llm_failed", error=str(result.error))
        return None

    raw = extract_json_payload(result.value.content)
    if raw is None:
        log.warning(
            "detector.llm_unparseable",
            preview=(result.value.content or "")[:200],
        )
        return None
    try:
        payload = json.loads(raw)
    except json.JSONDecodeError:
        return None
    if not isinstance(payload, dict):
        return None

    clean: dict[str, str] = {}
    for key in _COMMAND_KEYS:
        value = payload.get(key)
        if isinstance(value, str) and value.strip():
            clean[key] = value.strip()
    return clean


def _validate_proposal(
    working_dir: Path,
    proposal: dict[str, str],
) -> DetectedCommands:
    """Drop commands whose entry point cannot be verified on disk."""
    kept: dict[str, str | None] = dict.fromkeys(_COMMAND_KEYS)
    for key, command in proposal.items():
        if key not in _COMMAND_KEYS:
            continue
        if _command_is_valid(working_dir, command):
            kept[key] = command
        else:
            log.info(
                "detector.command_dropped",
                key=key,
                command=command,
                working_dir=str(working_dir),
            )
    return DetectedCommands(**kept)


_FORBIDDEN_TOKENS: tuple[str, ...] = ("&&", "||", "|", ";", ">", "<", "`", "$(")


def _command_is_valid(working_dir: Path, command: str) -> bool:
    """Return True iff ``command`` looks safely executable in ``working_dir``.

    Checks:
    1. No shell chaining / redirects (Stage 1 runs via ``create_subprocess_exec``).
    2. Leading token is on the allowlist.
    3. Script-style invocations (``npm run <name>``, ``make <target>``, etc.)
       reference a target that actually exists in the project's manifest.
    4. Bare binaries are on PATH.
    """
    if any(token in command for token in _FORBIDDEN_TOKENS):
        return False

    try:
        import shlex

        parts = shlex.split(command, posix=(os.name != "nt"))
    except ValueError:
        return False
    if not parts:
        return False

    head = Path(parts[0]).name
    if head not in _ALLOWED_EXECUTABLES:
        return False

    return _verify_entry_point(working_dir, head, parts)


def _verify_entry_point(
    working_dir: Path,
    head: str,
    parts: list[str],
) -> bool:
    """Confirm the script/target referenced by ``parts`` exists on disk.

    Bare binaries must resolve on PATH. Script runners must reference a
    declared script or target.
    """
    if head == "bun":
        if _bun_builtin_runner(parts):
            return True
    if head in {"npm", "pnpm", "yarn", "bun"}:
        script_name = _node_script_name(head, parts)
        if script_name is None:
            return False
        return _package_json_has_script(working_dir, script_name)

    if head == "npx":
        package = _npx_package(parts)
        if package is None:
            return False
        return _npx_package_available(working_dir, package)

    if head == "make":
        target = _make_target(parts)
        if target is None:
            return True
        return _makefile_has_target(working_dir, target)

    if head == "just":
        recipe = _first_positional(parts)
        if recipe is None:
            return False
        return _justfile_has_recipe(working_dir, recipe)

    if head == "uv":
        if not ((working_dir / "pyproject.toml").is_file() or (working_dir / "uv.lock").is_file()):
            return False
        return _uv_subcommand_is_available(working_dir, parts)

    if head == "cargo":
        if not (working_dir / "Cargo.toml").is_file():
            return False
        return _cargo_subcommand_is_available(parts)

    if head == "go":
        if not (working_dir / "go.mod").is_file():
            return False
        return _go_subcommand_is_builtin(parts)

    if head == "zig":
        if not (working_dir / "build.zig").is_file():
            return False
        return _zig_subcommand_is_builtin(parts)

    if head == "mvn":
        return (working_dir / "pom.xml").is_file()

    if head in {"mvnw", "mvnw.cmd"}:
        # Maven wrapper scripts are project-local — they must exist on disk
        # AND a pom.xml must be present. We accept ``./mvnw …`` (with the
        # relative prefix) or a bare ``mvnw …`` that is invoked from the
        # project root.
        if not (working_dir / "pom.xml").is_file():
            return False
        return (working_dir / head).is_file()

    if head in {"gradlew", "gradlew.bat"}:
        if not (
            (working_dir / "build.gradle").is_file() or (working_dir / "build.gradle.kts").is_file()
        ):
            return False
        return (working_dir / head).is_file()

    # Generic binary — require it on PATH. We do NOT require a manifest,
    # because standalone tools like pytest / ruff / mypy run from any repo.
    return shutil.which(head) is not None


