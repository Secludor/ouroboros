def _node_script_name(head: str, parts: list[str]) -> str | None:
    """Return the script name a Node package manager will resolve from ``parts``.

    The detector treats any bare token after ``<pm>`` as an implicit script
    (``yarn typecheck``, ``pnpm check`` …) so every proposal can be validated
    against ``package.json.scripts``. Explicit ``<pm> run <name>`` is also
    supported. Tokens that are package-manager built-ins (``install``,
    ``publish`` …) or flags return ``None`` so the caller can reject the
    command rather than execute something unverified.
    """
    if len(parts) < 2:
        return None
    second = parts[1]
    if second.startswith("-"):
        return None
    if second == "run":
        if len(parts) < 3 or parts[2].startswith("-"):
            return None
        return parts[2]
    if head == "npm":
        # npm only treats a small set of lifecycle names as script shortcuts;
        # everything else must go through ``npm run``.
        if second in _NPM_LIFECYCLE_SCRIPTS:
            return second
        return None
    builtins = {
        "pnpm": _PNPM_BUILTINS,
        "yarn": _YARN_BUILTINS,
        "bun": _BUN_BUILTINS,
    }.get(head, frozenset())
    if second in builtins:
        return None
    return second


def _package_json_has_script(working_dir: Path, name: str) -> bool:
    try:
        raw = (working_dir / "package.json").read_text(encoding="utf-8")
    except OSError:
        return False
    try:
        data = json.loads(raw)
    except json.JSONDecodeError:
        return False
    scripts = data.get("scripts") if isinstance(data, dict) else None
    return isinstance(scripts, dict) and name in scripts


def _make_target(parts: list[str]) -> str | None:
    """Return the first positional target in a ``make`` invocation."""
    for token in parts[1:]:
        if token.startswith("-"):
            continue
        if "=" in token:  # VAR=value overrides are not targets
            continue
        return token
    return None


def _makefile_has_target(working_dir: Path, target: str) -> bool:
    for name in ("Makefile", "makefile", "GNUmakefile"):
        path = working_dir / name
        if not path.is_file():
            continue
        try:
            for line in path.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if not stripped or stripped.startswith("#"):
                    continue
                # Match `target:` or `target :` at line start, skipping `.PHONY`.
                head = stripped.split(":", 1)[0].strip()
                if head == target:
                    return True
                # `.PHONY: build test` declarations also satisfy the check.
                if head == ".PHONY":
                    declared = stripped.split(":", 1)[1]
                    if target in declared.split():
                        return True
        except OSError:
            continue
    return False


def _render_toml(commands: DetectedCommands) -> str:
    """Serialize validated commands into ``mechanical.toml`` body.

    Values are written as TOML basic strings with ``\\`` and ``"`` escaped so
    commands like ``pytest -k "slow"`` survive the round-trip intact.
    """
    lines = [_DETECT_MARKER, ""]
    for key in _COMMAND_KEYS:
        value = getattr(commands, key)
        if value is None:
            continue
        lines.append(f"{key} = {_toml_basic_string(value)}")
    lines.append("")
    return "\n".join(lines)


_TOML_ESCAPES: dict[str, str] = {
    "\\": "\\\\",
    '"': '\\"',
    "\b": "\\b",
    "\t": "\\t",
    "\n": "\\n",
    "\f": "\\f",
    "\r": "\\r",
}


def _toml_basic_string(value: str) -> str:
    """Quote ``value`` as a TOML basic string with mandatory escapes."""
    out = [_TOML_ESCAPES.get(ch, ch) for ch in value]
    return '"' + "".join(out) + '"'


def _redact(proposal: dict[str, str]) -> dict[str, str]:
    """Truncate proposal values for safer logging."""
    return {k: (v[:120] if isinstance(v, str) else str(v)) for k, v in proposal.items()}


